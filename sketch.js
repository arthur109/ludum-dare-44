function draw() {
  if (inGame) {
    let frameTime = (1.0 / 60.0) * 1000.0;

    while (frameTimer > frameTime) {
      currentLevel.update();
      frameTimer -= frameTime;
    }

    currentLevel.update();
    currentLevel.draw();

    if (hasPaused) {
      inGame = false;
      currentUIPage = pausePage;
      hasPaused = true;
    }
  } else {
    if (hasUnPaused) {
      if (currentUIPage === pausePage) {
        inGame = true;
        previousUIPage = pausePage;
      }
    }
    currentUIPage.update();
  }
}

function keyPressed() {
  if (keyCode === 27 || keyCode === 80) {
    if (hasPaused) {
      hasPaused = false;
      hasUnPaused = true;
    } else {
      hasPaused = true;
      hasUnPaused = false;
    }
  }
}

function mouseReleased() {
  hasClicked = false;
}

